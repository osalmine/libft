int	ft_words(const char *s, char c)
{
	int words;

	words = 0;
	if (*s != c && *s)
	{
		s++;
		words++;
	}
	while (*s)
	{
		while (*s == c)
		{
			s++;
			if (*s != c && *s)
				words++;
		}
		s++;
	}
	return (words);
}
